    <li>Người có tiền sử mắc các bệnh lý về tim mạch, như suy tim nặng hoặc huyết áp cao không kiểm soát được.</li>
    <li>Người bệnh có bệnh lý về đường tiêu hóa như loét dạ dày tá tràng hoặc viêm đường tiêu hóa mãn tính.</li>
