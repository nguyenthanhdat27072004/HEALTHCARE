<p>Ibuprofen được chỉ định để làm giảm các triệu chứng đau như đau đầu, đau răng, đau bụng kinh, đau sau khi phẫu thuật, đau do chấn thương, và đau do viêm khớp. Ngoài ra, nó còn có tác dụng làm giảm viêm và hạ sốt, phù hợp cho các tình trạng như cảm lạnh và cúm.</p>
<h3 style="color: rgb(87, 87, 87);">Chống chỉ định sử dụng</h3>
<p>Ibuprofen không được khuyến cáo sử dụng trong một số trường hợp để tránh rủi ro sức khỏe không mong muốn. Các trường hợp chống chỉ định gồm:</p>
<ul>
