    <h2>Aspirin là thuốc gì?</h2>
    <p>Aspirin, hay còn được biết đến với tên khoa học là Acetylsalicylic Acid, là một loại thuốc có tác dụng giảm đau, hạ sốt, và chống viêm. Ngoài ra, Aspirin còn được biết đến với khả năng phòng ngừa các cơn đau tim và đột quỵ bởi khả năng làm loãng máu của nó.</p>
    <p>Aspirin được sử dụng rộng rãi trong điều trị các bệnh lý liên quan đến đau nhức và viêm như đau đầu, đau răng, đau khớp, và các tình trạng viêm khác. Đồng thời, nó cũng được dùng trong các chế độ điều trị để giảm nguy cơ tái phát nhồi máu cơ tim và đột quỵ ở những người đã có tiền sử bệnh tim mạch.</p>

    <h2>Chỉ định và chống chỉ định sử dụng Aspirin</h2>
    <p><strong>Chỉ định:</strong> Aspirin được chỉ định cho việc điều trị tạm thời của đau nhẹ đến vừa, giảm đau và viêm trong các bệnh lý như viêm khớp, và làm giảm sốt. Ngoài ra, Aspirin được sử dụng để ngăn ngừa đau tim và đột quỵ ở những người có nguy cơ cao.</p>
    <p><strong>Chống chỉ định:</strong> Aspirin không nên được sử dụng bởi những người có tiền sử dị ứng với salicylates, những người có vấn đề về chảy máu, như chứng rối loạn đông máu, hoặc những người có tiền sử loét dạ dày hoặc xuất huyết tiêu hóa.</p>

    <h2>Các dạng và hàm lượng thuốc Aspirin</h2>
    <p>Aspirin được sản xuất dưới nhiều hình thức khác nhau bao gồm viên nén, viên nang, và viên sủi bọt. Hàm lượng phổ biến bao gồm 81 mg (thường được gọi là liều thấp), 325 mg, và 500 mg.</p>

    <h2>Công dụng của Aspirin</h2>
    <p>Aspirin được sử dụng để giảm đau nhẹ đến vừa, giảm sốt, chống viêm, và đặc biệt là ngăn ngừa các sự kiện tim mạch như đau tim và đột quỵ ở người có nguy cơ cao.</p>

    <h2>Cách dùng và liều dùng Aspirin</h2>
    <p>Đối với giảm đau và hạ sốt: Người lớn thường được khuyến cáo uống 500 mg đến 1000 mg mỗi 4-6 giờ tùy thuộc vào mức độ cần thiết. Không nên vượt quá 4000 mg trong một ngày.</p>
    <p>Để phòng ngừa các biến cố tim mạch: Thông thường liều là 81 mg mỗi ngày, hoặc theo chỉ định của bác sĩ.</p>

    <h2>Tác dụng phụ của Aspirin</h2>
    <p>Aspirin có thể gây ra các tác dụng phụ bao gồm đau dạ dày, chảy máu tiêu hóa, buồn nôn, và hiếm hơn là dị ứng. Người dùng cũng cần lưu ý đến nguy cơ chảy máu cao khi sử dụng thuốc này, đặc biệt là khi dùng đồng thời với các thuốc khác có thể làm tăng nguy cơ chảy máu.</p>

    <h2>Lưu ý khi sử dụng thuốc Aspirin</h2>
    <p>Không dùng Aspirin cho trẻ em và thanh thiếu niên có các triệu chứng của bệnh cúm hoặc varicella do nguy cơ gây hội chứng Reye, một tình trạng hiếm gặp nhưng nghiêm trọng. Nên tham khảo ý kiến bác sĩ trước khi bắt đầu điều trị với Aspirin, đặc biệt là nếu bạn đang dùng các loại thuốc khác hoặc có các vấn đề sức khỏe hiện hữu.</p>
